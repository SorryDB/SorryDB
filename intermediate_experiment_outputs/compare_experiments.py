#!/usr/bin/env python3
"""
Comparison script for proof verification experiment analyses.

This script compares two analysis.json files (generated by analyze_proof_verification.py)
and creates a side-by-side comparison showing how each repository performed in both experiments.
"""

import json
import argparse
from pathlib import Path
from typing import Dict, List, Any, Optional

try:
    import matplotlib.pyplot as plt
    import matplotlib
    matplotlib.use('Agg')  # Use non-interactive backend
    MATPLOTLIB_AVAILABLE = True
except ImportError:
    MATPLOTLIB_AVAILABLE = False


def load_analysis(file_path: str, experiment_name: str) -> Dict[str, Any]:
    """Load and parse an analysis JSON file."""
    try:
        with open(file_path, 'r') as f:
            data = json.load(f)
        print(f"✓ Loaded analysis from {file_path} (Experiment: {experiment_name})")
        return data
    except FileNotFoundError:
        print(f"Error: File not found: {file_path}")
        exit(1)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in {file_path}: {e}")
        exit(1)


def build_repo_lookup(analysis: Dict[str, Any]) -> Dict[str, Dict[str, Any]]:
    """Build a lookup dictionary from repo URL to stats."""
    return {repo['repo']: repo for repo in analysis['by_repo']}


def compare_analyses(analysis1: Dict[str, Any], analysis2: Dict[str, Any],
                      name1: str, name2: str) -> Dict[str, Any]:
    """
    Compare two analyses and build a unified comparison structure.

    Returns a dictionary with comparison data including all repos from both analyses.
    """
    repo_lookup1 = build_repo_lookup(analysis1)
    repo_lookup2 = build_repo_lookup(analysis2)

    # Get union of all repos
    all_repos = set(repo_lookup1.keys()) | set(repo_lookup2.keys())

    comparison = []
    for repo in sorted(all_repos):
        stats1 = repo_lookup1.get(repo, {'total': 0, 'verified': 0, 'failed': 0, 'success_rate': 0.0})
        stats2 = repo_lookup2.get(repo, {'total': 0, 'verified': 0, 'failed': 0, 'success_rate': 0.0})

        # Calculate improvement
        rate_diff = stats2['success_rate'] - stats1['success_rate']
        verified_diff = stats2['verified'] - stats1['verified']

        comparison.append({
            'repo': repo,
            name1: {
                'total': stats1['total'],
                'verified': stats1['verified'],
                'failed': stats1['failed'],
                'success_rate': stats1['success_rate']
            },
            name2: {
                'total': stats2['total'],
                'verified': stats2['verified'],
                'failed': stats2['failed'],
                'success_rate': stats2['success_rate']
            },
            'improvement': {
                'rate_diff': round(rate_diff, 2),
                'verified_diff': verified_diff
            }
        })

    # Sort by absolute rate difference (biggest changes first)
    comparison.sort(key=lambda x: abs(x['improvement']['rate_diff']), reverse=True)

    return {
        'experiment_names': [name1, name2],
        'summaries': {
            name1: analysis1['summary'],
            name2: analysis2['summary']
        },
        'by_repo': comparison
    }


def write_json_output(comparison: Dict[str, Any], output_path: str):
    """Write comparison results to a JSON file."""
    with open(output_path, 'w') as f:
        json.dump(comparison, f, indent=2)
    print(f"✓ JSON comparison written to {output_path}")


def write_markdown_output(comparison: Dict[str, Any], output_path: str):
    """Write comparison results to a Markdown file."""
    lines = []
    name1, name2 = comparison['experiment_names']
    sum1 = comparison['summaries'][name1]
    sum2 = comparison['summaries'][name2]

    # Title
    lines.append(f"# Experiment Comparison: {name1} vs {name2}")
    lines.append("")

    # Summary comparison
    lines.append("## Summary Comparison")
    lines.append("")
    lines.append(f"| Metric | {name1} | {name2} | Difference |")
    lines.append("|--------|---------|---------|------------|")
    lines.append(f"| Total Repositories | {sum1['total_repos']} | {sum2['total_repos']} | {sum2['total_repos'] - sum1['total_repos']:+d} |")
    lines.append(f"| Total Sorries | {sum1['total_sorries']} | {sum2['total_sorries']} | {sum2['total_sorries'] - sum1['total_sorries']:+d} |")
    lines.append(f"| Total Verified | {sum1['total_verified']} | {sum2['total_verified']} | {sum2['total_verified'] - sum1['total_verified']:+d} |")
    lines.append(f"| Total Failed | {sum1['total_failed']} | {sum2['total_failed']} | {sum2['total_failed'] - sum1['total_failed']:+d} |")

    rate_diff = sum2['overall_success_rate'] - sum1['overall_success_rate']
    lines.append(f"| Overall Success Rate | {sum1['overall_success_rate']}% | {sum2['overall_success_rate']}% | {rate_diff:+.2f}% |")
    lines.append("")

    # Repository comparison table
    lines.append("## Results by Repository")
    lines.append("")
    lines.append(f"Sorted by absolute improvement (biggest changes first)")
    lines.append("")
    lines.append(f"| Repository | {name1} (V/T) | {name1} Rate | {name2} (V/T) | {name2} Rate | Δ Rate | Δ Verified |")
    lines.append("|-----------|---------------|--------------|---------------|--------------|--------|------------|")

    for entry in comparison['by_repo']:
        repo_name = entry['repo'].replace('https://github.com/', '')
        stats1 = entry[name1]
        stats2 = entry[name2]
        improvement = entry['improvement']

        # Format verified/total
        v_t_1 = f"{stats1['verified']}/{stats1['total']}" if stats1['total'] > 0 else "—"
        v_t_2 = f"{stats2['verified']}/{stats2['total']}" if stats2['total'] > 0 else "—"

        # Format rates
        rate1 = f"{stats1['success_rate']:.1f}%" if stats1['total'] > 0 else "—"
        rate2 = f"{stats2['success_rate']:.1f}%" if stats2['total'] > 0 else "—"

        # Format differences
        rate_diff_str = f"{improvement['rate_diff']:+.1f}%" if improvement['rate_diff'] != 0 else "—"
        verified_diff_str = f"{improvement['verified_diff']:+d}" if improvement['verified_diff'] != 0 else "—"

        lines.append(
            f"| {repo_name} | {v_t_1} | {rate1} | {v_t_2} | {rate2} | "
            f"{rate_diff_str} | {verified_diff_str} |"
        )

    lines.append("")

    # Add legend
    lines.append("## Legend")
    lines.append("")
    lines.append("- **V/T**: Verified / Total sorries")
    lines.append("- **Rate**: Success rate percentage")
    lines.append("- **Δ**: Change from first to second experiment")
    lines.append("- **—**: No data for this experiment")
    lines.append("")

    with open(output_path, 'w') as f:
        f.write('\n'.join(lines))

    print(f"✓ Markdown comparison written to {output_path}")


def generate_chart(comparison: Dict[str, Any], output_path: str):
    """Generate a grouped bar chart comparing success rates across repositories."""
    if not MATPLOTLIB_AVAILABLE:
        print("Warning: matplotlib not available. Install it or use: uv run --with matplotlib")
        return

    name1, name2 = comparison['experiment_names']

    # Prepare data
    repos = []
    rates1 = []
    rates2 = []

    for entry in comparison['by_repo']:
        # Use shortened repo name
        repo_name = entry['repo'].replace('https://github.com/', '')
        repos.append(repo_name)
        rates1.append(entry[name1]['success_rate'])
        rates2.append(entry[name2]['success_rate'])

    # Create figure with appropriate size for all repos
    fig, ax = plt.subplots(figsize=(20, 8))

    # Set up bar positions
    x = range(len(repos))
    width = 0.35

    # Create bars
    bars1 = ax.bar([i - width/2 for i in x], rates1, width, label=name1, alpha=0.8, color='#3498db')
    bars2 = ax.bar([i + width/2 for i in x], rates2, width, label=name2, alpha=0.8, color='#e74c3c')

    # Customize chart
    ax.set_xlabel('Repository', fontsize=12)
    ax.set_ylabel('Success Rate (%)', fontsize=12)
    ax.set_title('Proof Verification Success Rate Comparison by Repository', fontsize=14, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(repos, rotation=45, ha='right', fontsize=8)
    ax.legend(fontsize=10)
    ax.grid(axis='y', alpha=0.3, linestyle='--')
    ax.set_ylim(0, 105)  # Give a little headroom above 100%

    # Add value labels on bars for non-zero values
    def add_value_labels(bars, rates):
        for bar, rate in zip(bars, rates):
            if rate > 0:
                height = bar.get_height()
                ax.text(bar.get_x() + bar.get_width()/2., height,
                       f'{rate:.0f}%',
                       ha='center', va='bottom', fontsize=6)

    add_value_labels(bars1, rates1)
    add_value_labels(bars2, rates2)

    # Adjust layout to prevent label cutoff
    plt.tight_layout()

    # Save figure
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()

    print(f"✓ Chart written to {output_path}")


def derive_experiment_name(file_path: str) -> str:
    """
    Derive experiment name from file path.

    Examples:
      'analysis.json' -> 'analysis'
      'exp1_analysis.json' -> 'exp1_analysis'
      'path/to/baseline_analysis.json' -> 'baseline_analysis'
    """
    path = Path(file_path)
    # Remove .json extension
    name = path.stem
    return name


def main():
    parser = argparse.ArgumentParser(
        description='Compare two proof verification experiment analyses. '
                    'Experiment names are automatically derived from the filenames.'
    )
    parser.add_argument(
        'analysis1',
        help='Path to first analysis.json file'
    )
    parser.add_argument(
        'analysis2',
        help='Path to second analysis.json file'
    )
    parser.add_argument(
        '--output-json',
        default='comparison.json',
        help='Path for JSON output file (default: comparison.json)'
    )
    parser.add_argument(
        '--output-md',
        default='comparison.md',
        help='Path for Markdown output file (default: comparison.md)'
    )
    parser.add_argument(
        '--chart',
        action='store_true',
        help='Generate a comparison chart (requires matplotlib)'
    )
    parser.add_argument(
        '--output-chart',
        default='comparison_chart.png',
        help='Path for chart output file (default: comparison_chart.png)'
    )

    args = parser.parse_args()

    # Derive experiment names from filenames
    name1 = derive_experiment_name(args.analysis1)
    name2 = derive_experiment_name(args.analysis2)

    # Load both analyses
    print(f"Loading analyses...")
    analysis1 = load_analysis(args.analysis1, name1)
    analysis2 = load_analysis(args.analysis2, name2)

    # Compare
    print("Comparing experiments...")
    comparison = compare_analyses(analysis1, analysis2, name1, name2)

    # Print summary to console
    print("\n" + "="*60)
    print("COMPARISON SUMMARY")
    print("="*60)
    sum1 = comparison['summaries'][name1]
    sum2 = comparison['summaries'][name2]
    print(f"\n{name1}:")
    print(f"  Verified: {sum1['total_verified']}/{sum1['total_sorries']} ({sum1['overall_success_rate']}%)")
    print(f"\n{name2}:")
    print(f"  Verified: {sum2['total_verified']}/{sum2['total_sorries']} ({sum2['overall_success_rate']}%)")

    rate_diff = sum2['overall_success_rate'] - sum1['overall_success_rate']
    verified_diff = sum2['total_verified'] - sum1['total_verified']
    print(f"\nImprovement:")
    print(f"  Success Rate: {rate_diff:+.2f}%")
    print(f"  Verified Count: {verified_diff:+d}")
    print("="*60 + "\n")

    # Write outputs
    write_json_output(comparison, args.output_json)
    write_markdown_output(comparison, args.output_md)

    # Generate chart if requested
    if args.chart:
        if not MATPLOTLIB_AVAILABLE:
            print("\nWarning: Cannot generate chart - matplotlib not available")
            print("Run with: uv run --with matplotlib compare_experiments.py ...")
        else:
            generate_chart(comparison, args.output_chart)

    print("\n✓ Comparison complete!")


if __name__ == '__main__':
    main()
